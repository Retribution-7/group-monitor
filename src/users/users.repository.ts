import { inject, injectable } from 'inversify';
import { UserModel } from '../generated/prisma';
import { User } from './user.entity';
import { IUsersRepository } from './users.repository.interface';
import { TYPES } from '../types';
import { PrismaService } from '../database/prisma.service';

@injectable()
export class UsersRepository implements IUsersRepository {
	constructor(@inject(TYPES.PrismaService) private readonly prismaService: PrismaService) {}
	async find(email: string): Promise<UserModel | null> {
		return this.prismaService.client.userModel.findFirst({
			where: {
				email,
			},
		});
	}
	async create({ email, password, firstName, lastName, fathersName }: User): Promise<UserModel> {
		return this.prismaService.client.userModel.create({
			data: {
				email,
				password,
				firstName,
				lastName,
				fathersName,
			},
		});
	}
}
